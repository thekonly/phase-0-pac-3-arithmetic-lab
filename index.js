function add(a,b){
    return a+b;
}

function subtract(a,b){
    return a-b;
}

function multiply(a,b){
    return a*b;
}

function divide(a,b){
    return a/b;
}

function increment(a){
    return ++a;
}

function decrement(number){
    number-- ;
    return number;
}

function makeInt(n) {
    parseInt(n);
    return parseInt(n, 10);
}

function preserveDecimal(n) {
    parseFloat(n);
    return parseFloat(n);
}